public class FirstIfs{

    public static void main(String[] args){

        int number1=7, number2=1;

        if(number1 == number2){
            System.out.println("Number is equal to 1");
        }

        if(number1 != number2){
            System.out.println("Number is not equal to 1");
        }

        if(number1 > number2){
            System.out.println("Number is greater than 1");
        }

        if(number1 < number2){
            System.out.println("Number is less than 1");
        }
    }
}